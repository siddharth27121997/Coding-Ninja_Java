package Pattern;
import java.util.*;
public class Reversepattern {

	public static void main(String[] args) {
		
	Scanner sc = new Scanner(System.in);
	int n= sc.nextInt();
	int i =1;
	while(i<=n)
	{
		int j=4;
		while(j>=i)
		{
			System.out.print("*");
			
		j--;	
		}
		
		i++;
		System.out.println();
		
		
		
	}

		
		
		
		
	}

}
